def maps(a):
    # Định nghĩa hàm maps nhận vào một danh sách 'a'.

    return [number * 2 for number in a]
    # Sử dụng list comprehension để tạo một danh sách mới,
    # trong đó mỗi phần tử trong danh sách 'a' được nhân đôi bằng cách sử dụng biểu thức 'number * 2'.

# Hàm này cho phép bạn nhân đôi mỗi phần tử trong danh sách đầu vào 'a'
# và trả về một danh sách mới chứa các giá trị này.

